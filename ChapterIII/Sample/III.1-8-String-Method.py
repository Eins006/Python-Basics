# lower(),upper(),split(),count()
print('-'*50)
print('ABC'.lower())
print('def'.upper())
print('a b c'.split())
print('a,b,c'.split(','))
print('abcedfa'.count('a'))

# replace(),center()
print('-'*50)
print('python'.replace('p', 'C'))
print('python'.center(20, '='))

# strip(),join()
print('-'*50)
print(' python '.strip())
print(' python '.strip(' p'))
print(','.join("12345"))
